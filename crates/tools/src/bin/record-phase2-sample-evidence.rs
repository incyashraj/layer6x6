use std::env;
use std::fs;
use std::io::{Read, Write};
use std::net::{SocketAddr, TcpListener};
use std::path::{Path, PathBuf};
use std::process::{Command, Output};
use std::thread;
use std::time::{Duration, Instant, SystemTime, UNIX_EPOCH};

use anyhow::{bail, Context, Result};
use sha2::{Digest, Sha256};

fn main() -> Result<()> {
    let config = Config::parse(env::args().skip(1))?;
    let report = record_sample_evidence(&config)?;
    let markdown = report.to_markdown();

    if let Some(output) = &config.output {
        if let Some(parent) = output.parent() {
            fs::create_dir_all(parent)
                .with_context(|| format!("create output directory {}", parent.display()))?;
        }
        fs::write(output, markdown).with_context(|| format!("write {}", output.display()))?;
        println!("wrote {}", output.display());
    } else {
        print!("{markdown}");
    }

    Ok(())
}

#[derive(Debug, Clone)]
struct Config {
    layer36: PathBuf,
    clock: PathBuf,
    cat: PathBuf,
    curl: PathBuf,
    output: Option<PathBuf>,
}

impl Config {
    fn parse(args: impl IntoIterator<Item = String>) -> Result<Self> {
        let mut layer36 = None;
        let mut clock = None;
        let mut cat = None;
        let mut curl = None;
        let mut output = None;

        let mut args = args.into_iter();
        while let Some(arg) = args.next() {
            match arg.as_str() {
                "--layer36" => layer36 = Some(next_path(&mut args, "--layer36")?),
                "--clock" => clock = Some(next_path(&mut args, "--clock")?),
                "--cat" => cat = Some(next_path(&mut args, "--cat")?),
                "--curl" => curl = Some(next_path(&mut args, "--curl")?),
                "--output" => output = Some(next_path(&mut args, "--output")?),
                "--help" | "-h" => {
                    println!(
                        "Usage: record-phase2-sample-evidence --layer36 <path> --clock <wasm> --cat <wasm> --curl <wasm> [--output <md>]"
                    );
                    std::process::exit(0);
                }
                _ => bail!("unknown argument `{arg}`"),
            }
        }

        let config = Self {
            layer36: layer36.context("--layer36 is required")?,
            clock: clock.context("--clock is required")?,
            cat: cat.context("--cat is required")?,
            curl: curl.context("--curl is required")?,
            output,
        };
        config.validate_paths()?;
        Ok(config)
    }

    fn validate_paths(&self) -> Result<()> {
        for (name, path) in [
            ("layer36", &self.layer36),
            ("clock", &self.clock),
            ("cat", &self.cat),
            ("curl", &self.curl),
        ] {
            if !path.is_file() {
                bail!(
                    "{name} path does not exist or is not a file: {}",
                    path.display()
                );
            }
        }
        Ok(())
    }
}

fn next_path(args: &mut impl Iterator<Item = String>, flag: &str) -> Result<PathBuf> {
    args.next()
        .map(PathBuf::from)
        .with_context(|| format!("{flag} requires a path"))
}

#[derive(Debug, Clone)]
struct EvidenceReport {
    generated_unix_seconds: u64,
    git_commit: String,
    host_os: String,
    host_arch: String,
    rows: Vec<SampleRow>,
}

impl EvidenceReport {
    fn to_markdown(&self) -> String {
        let mut out = String::new();
        out.push_str("# Phase 2 Sample Evidence Run\n\n");
        out.push_str("This file is generated by `scripts/record-phase2-sample-evidence.sh`.\n\n");
        out.push_str("## Host\n\n");
        out.push_str(&format!("- Git commit: `{}`\n", self.git_commit));
        out.push_str(&format!(
            "- Host: `{}` / `{}`\n",
            self.host_os, self.host_arch
        ));
        out.push_str(&format!(
            "- Generated Unix time: `{}`\n\n",
            self.generated_unix_seconds
        ));

        out.push_str("## Results\n\n");
        out.push_str("| Sample | Status | Exit | Stdout SHA-256 | Stderr SHA-256 |\n");
        out.push_str("|---|---|---:|---|---|\n");
        for row in &self.rows {
            out.push_str(&format!(
                "| {} | {} | {} | `{}` | `{}` |\n",
                row.name,
                row.status,
                row.exit_code
                    .map(|code| code.to_string())
                    .unwrap_or_else(|| "n/a".to_string()),
                row.stdout_sha256.as_deref().unwrap_or("n/a"),
                row.stderr_sha256.as_deref().unwrap_or("n/a")
            ));
        }

        out.push_str("\n## Output Snapshots\n\n");
        for row in &self.rows {
            out.push_str(&format!("### {}\n\n", row.name));
            if let Some(reason) = &row.blocked_reason {
                out.push_str("Blocked:\n\n");
                out.push_str("```text\n");
                out.push_str(reason);
                if !reason.ends_with('\n') {
                    out.push('\n');
                }
                out.push_str("```\n\n");
                continue;
            }
            out.push_str("Command:\n\n");
            out.push_str("```text\n");
            out.push_str(&row.command);
            out.push_str("\n```\n\n");
            out.push_str("Stdout:\n\n");
            out.push_str("```text\n");
            out.push_str(&row.stdout);
            if !row.stdout.ends_with('\n') {
                out.push('\n');
            }
            out.push_str("```\n\n");
            if !row.stderr.is_empty() {
                out.push_str("Stderr:\n\n");
                out.push_str("```text\n");
                out.push_str(&row.stderr);
                if !row.stderr.ends_with('\n') {
                    out.push('\n');
                }
                out.push_str("```\n\n");
            }
        }

        out.push_str("## Reading The Result\n\n");
        out.push_str(
            "For Phase 2 exit, Linux, macOS, and Windows runs should show the same stdout hashes for `layer36-clock`, `layer36-cat`, and `layer36-curl`.\n",
        );
        out
    }
}

#[derive(Debug, Clone)]
struct SampleRow {
    name: String,
    status: String,
    command: String,
    exit_code: Option<i32>,
    stdout: String,
    stderr: String,
    stdout_sha256: Option<String>,
    stderr_sha256: Option<String>,
    blocked_reason: Option<String>,
}

fn record_sample_evidence(config: &Config) -> Result<EvidenceReport> {
    let mut rows = Vec::new();
    rows.push(run_clock(config)?);
    rows.push(run_cat(config)?);
    rows.push(run_curl(config)?);

    Ok(EvidenceReport {
        generated_unix_seconds: unix_seconds(),
        git_commit: git_commit(),
        host_os: env::consts::OS.to_string(),
        host_arch: env::consts::ARCH.to_string(),
        rows,
    })
}

fn run_clock(config: &Config) -> Result<SampleRow> {
    let args = [
        "run",
        "--test-time",
        "1234567890",
        "--test-locale",
        "en-US",
        "--test-timezone",
        "UTC",
    ];
    let output = Command::new(&config.layer36)
        .args(args)
        .arg(&config.clock)
        .output()
        .context("run layer36-clock evidence")?;

    row_from_output(
        "layer36-clock",
        command_text(&config.layer36, args, [&config.clock]),
        output,
        "app=layer36-clock\ntimezone=UTC\nlocale=en-US\ndate=1970-01-15 06:56\n",
    )
}

fn run_cat(config: &Config) -> Result<SampleRow> {
    let temp = EvidenceTempDir::new("layer36-phase2-cat")?;
    let fixtures = temp.path.join("fixtures");
    fs::create_dir(&fixtures).with_context(|| format!("create {}", fixtures.display()))?;
    fs::write(fixtures.join("a.txt"), "hello from Layer36 A\n")?;
    fs::write(fixtures.join("b.txt"), "hello from Layer36 B\n")?;

    let args = ["run", "--grant", "fs.read:fixtures/**"];
    let tail = ["--", "fixtures/a.txt", "fixtures/b.txt"];
    let output = Command::new(&config.layer36)
        .current_dir(&temp.path)
        .args(args)
        .arg(&config.cat)
        .args(tail)
        .output()
        .context("run layer36-cat evidence")?;

    let command = command_text_with_cwd(&config.layer36, &temp.path, args, [&config.cat], tail);
    row_from_output(
        "layer36-cat",
        command,
        output,
        "hello from Layer36 A\nhello from Layer36 B\n",
    )
}

fn run_curl(config: &Config) -> Result<SampleRow> {
    let body = b"hello from Layer36 curl\n";
    let Some((addr, handle)) = spawn_http_fixture(body)? else {
        return Ok(blocked_row(
            "layer36-curl",
            "localhost HTTP fixture bind is blocked in this environment",
        ));
    };
    let url = format!("http://{addr}/fixture.txt");
    let grant = format!("net.connect:{addr}");

    let output = Command::new(&config.layer36)
        .args(["run", "--grant", &grant])
        .arg(&config.curl)
        .args(["--", &url])
        .output()
        .context("run layer36-curl evidence")?;

    let accepted = handle
        .join()
        .map_err(|_| anyhow::anyhow!("HTTP fixture thread panicked"))?;
    if !accepted {
        return Ok(blocked_row(
            "layer36-curl",
            "localhost HTTP fixture was not reached before timeout",
        ));
    }

    row_from_output(
        "layer36-curl",
        command_text(
            &config.layer36,
            ["run", "--grant", grant.as_str()],
            [&config.curl],
        ) + &format!(" -- {url}"),
        output,
        "hello from Layer36 curl\n",
    )
}

fn row_from_output(
    name: &str,
    command: String,
    output: Output,
    expected_stdout: &str,
) -> Result<SampleRow> {
    let exit_code = output.status.code().unwrap_or(-1);
    let stdout = String::from_utf8(output.stdout).context("sample stdout must be UTF-8")?;
    let stderr = String::from_utf8(output.stderr).context("sample stderr must be UTF-8")?;

    if exit_code != 0 {
        bail!("{name} exited with {exit_code}\nstdout:\n{stdout}\nstderr:\n{stderr}");
    }
    if stdout != expected_stdout {
        bail!("{name} stdout drifted\nexpected:\n{expected_stdout}\nactual:\n{stdout}");
    }
    if !stderr.is_empty() {
        bail!("{name} wrote unexpected stderr:\n{stderr}");
    }

    Ok(SampleRow {
        name: name.to_string(),
        status: "passed".to_string(),
        command,
        exit_code: Some(exit_code),
        stdout_sha256: Some(sha256_hex(stdout.as_bytes())),
        stderr_sha256: Some(sha256_hex(stderr.as_bytes())),
        stdout,
        stderr,
        blocked_reason: None,
    })
}

fn blocked_row(name: &str, reason: &str) -> SampleRow {
    SampleRow {
        name: name.to_string(),
        status: "blocked".to_string(),
        command: String::new(),
        exit_code: None,
        stdout: String::new(),
        stderr: String::new(),
        stdout_sha256: None,
        stderr_sha256: None,
        blocked_reason: Some(reason.to_string()),
    }
}

fn spawn_http_fixture(
    body: &'static [u8],
) -> Result<Option<(SocketAddr, thread::JoinHandle<bool>)>> {
    let listener = match TcpListener::bind("127.0.0.1:0") {
        Ok(listener) => listener,
        Err(err) if err.kind() == std::io::ErrorKind::PermissionDenied => return Ok(None),
        Err(err) => return Err(err).context("bind localhost HTTP fixture"),
    };
    listener
        .set_nonblocking(true)
        .context("set HTTP fixture nonblocking")?;
    let addr = listener.local_addr().context("read HTTP fixture address")?;
    let handle = thread::spawn(move || {
        let deadline = Instant::now() + Duration::from_secs(5);
        let mut stream = loop {
            match listener.accept() {
                Ok((stream, _)) => break stream,
                Err(err) if err.kind() == std::io::ErrorKind::WouldBlock => {
                    if Instant::now() >= deadline {
                        return false;
                    }
                    thread::sleep(Duration::from_millis(10));
                }
                Err(_) => return false,
            }
        };
        if stream.set_nonblocking(false).is_err() {
            return false;
        }
        let mut request = [0_u8; 1024];
        if stream.read(&mut request).is_err() {
            return false;
        }
        if write!(
            stream,
            "HTTP/1.1 200 OK\r\nContent-Length: {}\r\nContent-Type: text/plain\r\nConnection: close\r\n\r\n",
            body.len()
        )
        .is_err()
        {
            return false;
        }
        stream.write_all(body).is_ok()
    });

    Ok(Some((addr, handle)))
}

struct EvidenceTempDir {
    path: PathBuf,
}

impl EvidenceTempDir {
    fn new(prefix: &str) -> Result<Self> {
        let path = env::temp_dir().join(format!(
            "{prefix}-{}-{}",
            std::process::id(),
            unix_seconds()
        ));
        fs::create_dir(&path).with_context(|| format!("create {}", path.display()))?;
        Ok(Self { path })
    }
}

impl Drop for EvidenceTempDir {
    fn drop(&mut self) {
        let _ = fs::remove_dir_all(&self.path);
    }
}

fn command_text<const N: usize, const M: usize>(
    binary: &Path,
    args: [&str; N],
    paths: [&PathBuf; M],
) -> String {
    let mut parts = vec![binary.display().to_string()];
    parts.extend(args.into_iter().map(quote_arg));
    parts.extend(
        paths
            .into_iter()
            .map(|path| quote_arg(&path.display().to_string())),
    );
    parts.join(" ")
}

fn command_text_with_cwd<const N: usize, const M: usize, const T: usize>(
    binary: &Path,
    cwd: &Path,
    args: [&str; N],
    paths: [&PathBuf; M],
    tail: [&str; T],
) -> String {
    let mut command = format!("(cd {} && ", quote_arg(&cwd.display().to_string()));
    command.push_str(&command_text(binary, args, paths));
    for arg in tail {
        command.push(' ');
        command.push_str(&quote_arg(arg));
    }
    command.push(')');
    command
}

fn quote_arg(value: &str) -> String {
    if value
        .chars()
        .all(|ch| ch.is_ascii_alphanumeric() || "-_./:=+".contains(ch))
    {
        value.to_string()
    } else {
        format!("'{}'", value.replace('\'', "'\\''"))
    }
}

fn sha256_hex(bytes: &[u8]) -> String {
    let digest = Sha256::digest(bytes);
    digest.iter().map(|byte| format!("{byte:02x}")).collect()
}

fn unix_seconds() -> u64 {
    SystemTime::now()
        .duration_since(UNIX_EPOCH)
        .unwrap_or_default()
        .as_secs()
}

fn git_commit() -> String {
    let Ok(output) = Command::new("git")
        .args(["rev-parse", "--short", "HEAD"])
        .output()
    else {
        return "unknown".to_string();
    };
    if !output.status.success() {
        return "unknown".to_string();
    }
    String::from_utf8_lossy(&output.stdout).trim().to_string()
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn sha256_hex_matches_known_value() {
        assert_eq!(
            sha256_hex(b"hello\n"),
            "5891b5b522d5df086d0ff0b110fbd9d21bb4fc7163af34d08286a2e846f6be03"
        );
    }

    #[test]
    fn markdown_includes_gate_reading_note() {
        let report = EvidenceReport {
            generated_unix_seconds: 1,
            git_commit: "abc1234".to_string(),
            host_os: "linux".to_string(),
            host_arch: "x86_64".to_string(),
            rows: vec![SampleRow {
                name: "layer36-clock".to_string(),
                status: "passed".to_string(),
                command: "layer36 run clock.wasm".to_string(),
                exit_code: Some(0),
                stdout: "ok\n".to_string(),
                stderr: String::new(),
                stdout_sha256: Some(sha256_hex(b"ok\n")),
                stderr_sha256: Some(sha256_hex(b"")),
                blocked_reason: None,
            }],
        };

        let markdown = report.to_markdown();

        assert!(markdown.contains("# Phase 2 Sample Evidence Run"));
        assert!(markdown.contains("For Phase 2 exit"));
        assert!(markdown.contains("layer36-clock"));
    }
}
