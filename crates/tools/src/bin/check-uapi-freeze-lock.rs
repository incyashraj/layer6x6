use std::env;
use std::fs;
use std::path::{Path, PathBuf};

use anyhow::{bail, Context, Result};
use sha2::{Digest, Sha256};

fn main() -> Result<()> {
    let mode = Mode::parse(env::args().skip(1))?;
    let root = workspace_root();
    let lock_path = root.join("docs/book/src/phase2/uapi-freeze-lock.md");
    let report = render_lock(&root)?;

    match mode {
        Mode::Check => {
            let current = fs::read_to_string(&lock_path)
                .with_context(|| format!("read {}", lock_path.display()))?;
            if current != report {
                bail!(
                    "UAPI freeze lock is stale. Run `scripts/generate-uapi-freeze-lock.sh` and review the WIT diff."
                );
            }
            println!("Layer36 Phase 2 UAPI freeze lock is current");
            println!("- page: {}", lock_path.display());
        }
        Mode::Write => {
            fs::write(&lock_path, report)
                .with_context(|| format!("write {}", lock_path.display()))?;
            println!("wrote {}", lock_path.display());
        }
    }

    Ok(())
}

enum Mode {
    Check,
    Write,
}

impl Mode {
    fn parse(args: impl IntoIterator<Item = String>) -> Result<Self> {
        let mut mode = Self::Check;

        for arg in args {
            match arg.as_str() {
                "--check" => mode = Self::Check,
                "--write" => mode = Self::Write,
                "--help" | "-h" => {
                    println!("Usage: check-uapi-freeze-lock [--check|--write]");
                    std::process::exit(0);
                }
                _ => bail!("unknown argument `{arg}`"),
            }
        }

        Ok(mode)
    }
}

struct WitFile {
    path: String,
    bytes: usize,
    lines: usize,
    sha256: String,
}

fn render_lock(root: &Path) -> Result<String> {
    let wit_root = root.join("wit/layer36/phase2");
    let files = collect_wit_files(root, &wit_root)?;
    let aggregate_sha256 = aggregate_sha256(&files);

    let mut out = String::new();
    out.push_str("# UAPI Freeze Lock\n\n");
    out.push_str("> Generated by `scripts/generate-uapi-freeze-lock.sh`.\n\n");
    out.push_str("Status: freeze candidate lock.\n\n");
    out.push_str(
        "This file records the exact Phase 2 WIT files being reviewed for UAPI v0.1. It does not replace the human freeze decision. It makes contract drift visible before that decision and after it.\n\n",
    );
    out.push_str("## Summary\n\n");
    out.push_str("- Package root: `wit/layer36/phase2`\n");
    out.push_str(&format!("- WIT files: {}\n", files.len()));
    out.push_str(&format!("- Aggregate SHA-256: `{aggregate_sha256}`\n\n"));
    out.push_str("## Files\n\n");
    out.push_str("| File | Lines | Bytes | SHA-256 |\n");
    out.push_str("|---|---:|---:|---|\n");

    for file in &files {
        out.push_str(&format!(
            "| `{}` | {} | {} | `{}` |\n",
            file.path, file.lines, file.bytes, file.sha256
        ));
    }

    out.push_str("\n## Review Rule\n\n");
    out.push_str(
        "If any hash changes, the Phase 2 UAPI changed. Review the WIT diff, regenerate this lock intentionally, regenerate the UAPI reference and freeze evidence, then record the reason in the Phase 2 plan.\n",
    );

    Ok(out)
}

fn collect_wit_files(root: &Path, dir: &Path) -> Result<Vec<WitFile>> {
    let mut paths = Vec::new();
    collect_wit_paths(dir, &mut paths)?;
    paths.sort();

    paths
        .into_iter()
        .map(|path| {
            let bytes = fs::read(&path).with_context(|| format!("read {}", path.display()))?;
            let rel = path
                .strip_prefix(root)
                .with_context(|| format!("make {} relative", path.display()))?
                .to_string_lossy()
                .replace('\\', "/");
            let text = String::from_utf8_lossy(&bytes);
            let lines = text.lines().count();
            Ok(WitFile {
                path: rel,
                bytes: bytes.len(),
                lines,
                sha256: sha256_hex(&bytes),
            })
        })
        .collect()
}

fn collect_wit_paths(dir: &Path, paths: &mut Vec<PathBuf>) -> Result<()> {
    for entry in fs::read_dir(dir).with_context(|| format!("read dir {}", dir.display()))? {
        let entry = entry.with_context(|| format!("read entry in {}", dir.display()))?;
        let path = entry.path();
        if path.is_dir() {
            collect_wit_paths(&path, paths)?;
        } else if path.extension().and_then(|ext| ext.to_str()) == Some("wit") {
            paths.push(path);
        }
    }

    Ok(())
}

fn aggregate_sha256(files: &[WitFile]) -> String {
    let mut hasher = Sha256::new();
    for file in files {
        hasher.update(file.path.as_bytes());
        hasher.update([0]);
        hasher.update(file.sha256.as_bytes());
        hasher.update([b'\n']);
    }
    hex(hasher.finalize().as_slice())
}

fn sha256_hex(bytes: &[u8]) -> String {
    let mut hasher = Sha256::new();
    hasher.update(bytes);
    hex(hasher.finalize().as_slice())
}

fn hex(bytes: &[u8]) -> String {
    bytes.iter().map(|byte| format!("{byte:02x}")).collect()
}

fn workspace_root() -> PathBuf {
    Path::new(env!("CARGO_MANIFEST_DIR"))
        .join("../..")
        .canonicalize()
        .expect("workspace root")
}

